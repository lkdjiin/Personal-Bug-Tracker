    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        project = (Project) projects.getSelectedValue();
        try {
            if (ProjectDAO.isArchived(project.pro_name)) {
                String msg = I18N.getBundle("A_project_") + " " + project.pro_name + " "
                        + I18N.getBundle("_is_already_archived");
                msg += I18N.getBundle("Archiving_is_canceled");
                JOptionPane.showMessageDialog(this, msg);
                return;
            }
        } catch (SQLException ex) {
            Logger.getLogger(DialogArchive.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        boolean result = false;
        try {
            Archiving archiving = new Archiving(project);
            result = archiving.archive();
        } catch (SqlTransactionException ex) {
            Logger.getLogger(DialogArchive.class.getName()).log(Level.SEVERE, null, ex);
        }
        String msg = null;
        if (result == true) {
            msg = I18N.getBundle("Project_") + " " + project.pro_name + " " + I18N.getBundle("_archived.");
        } else {
            msg = I18N.getBundle("Error_:_no_archiving_and_no_deletion.");
        }
        this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
        JOptionPane.showMessageDialog(this, msg);
        doClose(RET_OK);
    }//GEN-LAST:event_okButtonActionPerformed

