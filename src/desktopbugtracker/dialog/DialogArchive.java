    public DialogArchive(java.awt.Frame parent, Project project) {
        super(parent, true);
        if (project == null) {
            JOptionPane.showMessageDialog(parent, I18N.getBundle("Error_:_null_project"));
            return;
        }
        dlm = new DefaultListModel();
        Vector<Project> vProject = new Vector<Project>();
        vProject = Project.readWithoutArchive();
        int selectedProject = 0;
        for (Project e : vProject) {
            dlm.addElement(e);
            if (e.pro_name.equals(project.pro_name)) {
                selectedProject = dlm.getSize() - 1;
            }
        }

        initComponents();
        this.getRootPane().setDefaultButton(okButton);
        projects.setSelectedIndex(selectedProject);

        setLocationRelativeTo(parent);
        setVisible(true);
    }

