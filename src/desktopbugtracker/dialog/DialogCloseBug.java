    public void populateVersion() {
        cbVersionModel.removeAllElements();
        Vector<Version> vVersion = new Vector<Version>();
        try {
            vVersion = VersionDAO.read("pro_name", bug.pro_name);
        } catch (SQLException ex) {
            Logger.getLogger(DialogCloseBug.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (Version e : vVersion) {
            cbVersionModel.addElement(e);
        }
    }

    /** @return the return status of this dialog - one of RET_OK or RET_CANCEL */
