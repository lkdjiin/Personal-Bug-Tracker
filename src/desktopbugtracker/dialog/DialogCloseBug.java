    private void btNewVersionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNewVersionActionPerformed
        Project project = null;
        try {
            project = ProjectDAO.readPk(bug.pro_name);
        } catch (SQLException ex) {
            Logger.getLogger(DialogCloseBug.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (project == null) {
            JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("desktopbugtracker/Bundle", ApplicationConfig.getLocale()).getString("Error_while_reading_project."));
        } else {
            try {
                new DialogNewVersion(parent, true, project);
            } catch (SQLException ex) {
                Logger.getLogger(DialogCloseBug.class.getName()).log(Level.SEVERE, null, ex);
            }
            populateVersion();
        }
    }//GEN-LAST:event_btNewVersionActionPerformed

    private void doClose(int retStatus) {
        returnStatus = retStatus;
        setVisible(false);
        dispose();
    }
