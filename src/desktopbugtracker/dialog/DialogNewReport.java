    public DialogNewReport(java.awt.Frame parent, boolean modal, Priority priority, Project project) throws SQLException {
        super(parent, modal);
        assert priority != null;
        assert project != null;
        this.parent = parent;
        this.project = project;
        cbPriorityModel = new DefaultComboBoxModel();
        Vector<Priority> vPriority = PriorityDAO.read();
        for (Priority e : vPriority) {
            // La priorité zéro (View All) est spéciale, elle ne sert que pour la
            // consultation.
            if (e.pri_number != 0) {
                cbPriorityModel.addElement(e);
            }
        }
        if (priority != null) {
            // Si c'est la priorité zéro, il faut changer.
            if (priority.pri_number == 0) {
                priority = PriorityDAO.getHighestPriority();
            }
            cbPriorityModel.setSelectedItem(priority);
        }

        cbVersionModel = new DefaultComboBoxModel();

        populateVersion();

        cbCategoryModel = new DefaultComboBoxModel();
        Vector<Category> vCategory = CategoryDAO.read(project.pro_name);
       

        initComponents();

         if (vCategory.isEmpty()) {
            cbCategory.setEnabled(false);
        } else {
            for (Category e : vCategory) {
                cbCategoryModel.addElement(e);
            }
        }

        lblProjectName.setText(project.pro_name);

        this.getRootPane().setDefaultButton(okButton);
        setLocationRelativeTo(parent);
        setVisible(true);
    }

    private void populateVersion() throws SQLException {
        if (project != null) {
            cbVersionModel.removeAllElements();
            Vector<Version> vVersion = VersionDAO.read("pro_name", project.pro_name);
            for (Version e : vVersion) {
                cbVersionModel.addElement(e);
            }

        }
    }

