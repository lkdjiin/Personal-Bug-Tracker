    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        try {
            int cat_id = 0;
            if (cbCategory.isEnabled())
                cat_id = ((Category)cbCategoryModel.getSelectedItem()).cat_id;
            int bug_id = BugDAO.getNextId();
            Bug bug = new Bug(bug_id, txtTitle.getText(), new Date().getTime(), 0,
                    ((Version) cbVersionModel.getSelectedItem()).ver_name, "",
                    ((Priority) cbPriorityModel.getSelectedItem()).pri_number, project.pro_name, cat_id, 0);
            Message message = new Message(MessageDAO.getNextId(), txtMessage.getText(), new Date().getTime(),
                    new Date().getTime(), bug_id, 0);
            BugDAO.create(bug);
            MessageDAO.create(message);
        } catch (SQLException ex) {
            Logger.getLogger(DialogNewReport.class.getName()).log(Level.SEVERE, null, ex);
        }
        doClose(RET_OK);
    }//GEN-LAST:event_okButtonActionPerformed

