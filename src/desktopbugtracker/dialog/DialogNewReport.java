    private void checkEntries() {
        if (txtTitle.getText().equals("") 
                || txtMessage.getText().equals("")
                || cbVersionModel.getSize() == 0) {
            okButton.setEnabled(false);
        } else {
            okButton.setEnabled(true);
        }
    }

    private void btNewVersionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNewVersionActionPerformed

        if (project == null) {
            JOptionPane.showMessageDialog(this,
                    java.util.ResourceBundle.getBundle(
                    "desktopbugtracker/Bundle",
                    ApplicationConfig.getLocale()).getString("Error_:_there_is_no_project."));
        } else {
            try {
                DialogNewVersion dial = new DialogNewVersion(parent, true,
                        project);
                populateVersion();
            } catch (SQLException ex) {
                Logger.getLogger(DialogNewReport.class.getName()).log(
                        Level.SEVERE, null, ex);
            }
            checkEntries();
        }
    }//GEN-LAST:event_btNewVersionActionPerformed

